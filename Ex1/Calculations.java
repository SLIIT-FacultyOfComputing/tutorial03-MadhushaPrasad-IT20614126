class Calculations{

    //create static method to add two numbers
    public static void additionOfTwoNumbers(int num1 , int num2){
        System.out.println("Addition of two numbers is :" + (num1 + num2));
    }

    //create static method to subtract two numbers
    public static void subtractionOfTwoNumbers(int num1 , int num2){
        System.out.println("Subtraction of two numbers is :" + (num1 - num2));
    }
}